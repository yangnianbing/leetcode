/**
 * // Definition for a Node.
 * function Node(val,next,random) {
 *    this.val = val;
 *    this.next = next;
 *    this.random = random;
 * };
 */
/**
 * @param {Node} head
 * @return {Node}
 */
var copyRandomList = function(head) {
    return cloneNode(head, new Map());

    function cloneNode(node, map){
        if(node){
            let newNode = map.get(node);
            if(!newNode){
                newNode = {val: node.val};
                map.set(node, newNode);
                newNode.next = cloneNode(node.next, map);
                newNode.random = cloneNode(node.random, map);
            }
            return  newNode;
        }
        return null;
    }
};


